# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

n1 = float(input('Digite um valor: '))
n2 = n1 * n1
n3 = n2 * 2
print('O valor escolhido por você foi {}, e a área de um quadrado deste valor é de {}, logo o dobro da área deste quadrado é de {}!'.format(n1, n2, n3))